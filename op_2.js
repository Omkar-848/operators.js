// Equal to (==)
//comparison (Relational) operators

let a = 10;
let b = "10";
console.log(a === b);

// Comparison operators examples all types

// Equal to (==)
console.log(5 == "5"); // true (compares value, not type)

// Strict equal to (===)
console.log(5 === "5"); // false (compares value and type)

// Not equal to (!=)
console.log(5 != "5"); // false (compares value, not type)

// Strict not equal to (!==)
console.log(5 !== "5"); // true (compares value and type)

// Greater than (>)
console.log(10 > 5); // true

// Less than (<)
console.log(10 < 5); // false

// Greater than or equal to (>=)
console.log(10 >= 10); // true

// Less than or equal to (<=)
console.log(5 <= 10); // true
